function submit_mouseover (iditem)
{
    var vIditem = document.getElementById(iditem); // se hace la referencia
    vIditem.style.background = "#F5F5F5"; //"#47B013";
}

function submit_mouseout (iditem)
{
    var vIditem = document.getElementById(iditem); // se hace la referencia
    vIditem.style.background = white;
}


